/*******************************************************************************
 * 函数名  : RF_INTF_APCChange
 * 描  述  : APC值切换处理
 * 输  入  : u16Target:目标值，u16Src:源值
 * 输  出  : 无
 * 返回值  : 无
 *******************************************************************************/
DH_VOID  RF_INTF_APCChange(DH_U16 u16Target, DH_U16 u16Src)
{
    DH_U8 i;
    DH_U16 ValueMnt;
	if (u16Target >= u16Src)
	{
		for(i=0;i<180;i++)
		{
			 ValueMnt =(DH_U16)((u16Target*APC_Step_Val[i])>>11);
			 RF_INTF_APCSet(ValueMnt);
			 Delay_us(4);
		}
	}
	else
	{
		for(i=0;i<180;i++)
		{
			 ValueMnt =(DH_U16)((u16Src*APC_Step_Val[179-i])>>11);
			 RF_INTF_APCSet(ValueMnt);
			 Delay_us(4);
		}
	}

}

