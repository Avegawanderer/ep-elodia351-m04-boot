/***************************************************************************
* 函数名：RF_INTF_GetRssiLevelStat
* 描  述：获取RSSI门限
* 输  入：无
* 输  出：0:无信号，非0有信号
* 返回值：无
***************************************************************************/
DH_U8 RF_INTF_GetRssiLevelStat(DH_VOID)
{
	DH_U8 i;
	DH_U32 SignalValue=0;
//	return 0;
	for(i=0;i<8;i++)
	{
		SignalValue += RF_INTF_GetSignal();

		Delay_ms(10);
	}

	SignalValue >>= 3;/*除以8，求均值*/
//	printk("SignalValue %d.",SignalValue);
	if(SignalValue < RF_RSSI_LEVEL)
	{
		return 0;
	}
	else
	{
		return 1;
	}
}


